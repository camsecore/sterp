      const avatar_url = `https://images.sterp.com/${filePath}?t=${Date.now()}`;

      const { error: updateError } = await supabase
        .from("users")
        .update({ avatar_url })
        .eq("id", user.id);

      if (updateError) throw new Error(updateError.message);

      setProfile((prev) => prev ? { ...prev, avatar_url } : prev);
