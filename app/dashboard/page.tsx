      const supabase = createClient();
      const filePath = `${user.id}/avatar.webp`;

      const { error: uploadError } = await supabase.storage
        .from("product-photos")
        .upload(filePath, webpBlob, {
          contentType: "image/webp",
          upsert: true,
        });

