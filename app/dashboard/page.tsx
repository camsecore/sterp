interface CropModalProps {
  imageSrc: string;
  aspect: number;
  onDone: (croppedBlob: Blob) => void;
  onCancel: () => void;
}

function CropModal({ imageSrc, aspect, onDone, onCancel }: CropModalProps) {
  const [currentSrc, setCurrentSrc] = useState(imageSrc);
  const [crop, setCrop] = useState({ x: 0, y: 0 });
  const [zoom, setZoom] = useState(1);
  const [croppedArea, setCroppedArea] = useState<Area | null>(null);

  const handleCropComplete = useCallback((_: Area, croppedAreaPixels: Area) => {
    setCroppedArea(croppedAreaPixels);
  }, []);

  function handleRotate() {
    const img = new globalThis.Image();
    img.onload = () => {
      const canvas = document.createElement("canvas");
      canvas.width = img.height;
      canvas.height = img.width;
      const ctx = canvas.getContext("2d");
      if (!ctx) return;
      ctx.translate(canvas.width / 2, canvas.height / 2);
      ctx.rotate(Math.PI / 2);
      ctx.drawImage(img, -img.width / 2, -img.height / 2);
      canvas.toBlob((blob) => {
        if (!blob) return;
        if (currentSrc !== imageSrc) URL.revokeObjectURL(currentSrc);
        const url = URL.createObjectURL(blob);
        setCurrentSrc(url);
        setCrop({ x: 0, y: 0 });
        setZoom(1);
      }, "image/jpeg", 0.95);
    };
    img.src = currentSrc;
  }

  async function handleDone() {
    if (!croppedArea) return;
    const blob = await getCroppedBlob(currentSrc, croppedArea);
    if (currentSrc !== imageSrc) URL.revokeObjectURL(currentSrc);
    onDone(blob);
  }

  return (
    <div role="dialog" aria-modal="true" aria-label="Crop image" className="fixed inset-0 z-[60] bg-black flex flex-col">
      <div className="relative flex-1">
        <Cropper
          image={currentSrc}
          crop={crop}
          zoom={zoom}
          aspect={aspect}
          onCropChange={setCrop}
          onZoomChange={setZoom}
          onCropComplete={handleCropComplete}
          showGrid={true}
        />
      </div>
      <div className="flex items-center gap-4 px-6 py-4 pb-[max(1rem,env(safe-area-inset-bottom))] bg-black">
        <button
          type="button"
          onClick={handleRotate}
          className="text-white/70 hover:text-white p-1"
          aria-label="Rotate 90°"
        >
          <RotateCw size={20} />
        </button>
        <input
          type="range"
          min={1}
          max={3}
          step={0.1}
          value={zoom}
          onChange={(e) => setZoom(Number(e.target.value))}
          className="flex-1 accent-white"
        />
        <button
          type="button"
          onClick={onCancel}
          className="text-[15px] text-white/70 hover:text-white"
        >
          Cancel
        </button>
        <button
          type="button"
          onClick={handleDone}
          className="text-[15px] font-medium text-white bg-[#C0392B] px-6 py-2 rounded-md hover:opacity-90"
        >
          Done
        </button>
      </div>
    </div>
  );
}

// ─── Hybrid Dropdown ────────────────────────────────────────────────
// Identical trigger UI on all devices. Desktop: custom menu. Mobile: native
// <select> overlaid invisibly on the trigger to invoke the OS scroll picker.

function useIsTouchDevice() {
  const [isTouch, setIsTouch] = useState<boolean | null>(null);
  useEffect(() => {
    setIsTouch(
      window.matchMedia("(pointer: coarse)").matches || navigator.maxTouchPoints > 0
    );
  }, []);
  return isTouch;
}

function CustomDropdown({
  value,
  options,
  onChange,
}: {
  value: string;
  options: { value: string; label: string }[];
  onChange: (value: string) => void;
}) {
  const isTouch = useIsTouchDevice();
  const [open, setOpen] = useState(false);
  const triggerRef = useRef<HTMLButtonElement>(null);
  const listRef = useRef<HTMLUListElement>(null);
  const [pos, setPos] = useState<{ top: number; left: number; minWidth: number } | null>(null);

  // Position the portal dropdown relative to the trigger
  useEffect(() => {
    if (!open || !triggerRef.current) return;
    const rect = triggerRef.current.getBoundingClientRect();
    setPos({ top: rect.bottom + 4, left: rect.left, minWidth: rect.width });
  }, [open]);

  // Close on outside click
  useEffect(() => {
    if (!open) return;
    function handleClick(e: MouseEvent) {
      if (
        triggerRef.current && !triggerRef.current.contains(e.target as Node) &&
        listRef.current && !listRef.current.contains(e.target as Node)
      ) {
        setOpen(false);
      }
    }
    document.addEventListener("mousedown", handleClick);
    return () => document.removeEventListener("mousedown", handleClick);
  }, [open]);

  const selectedLabel = options.find((o) => o.value === value)?.label ?? value;

  return (
    <div className="relative">
      {/* Shared trigger — always visible */}
      <button
        ref={triggerRef}
        type="button"
        onClick={isTouch ? undefined : () => setOpen(!open)}
        className="flex items-center gap-1.5 bg-gray-50 border border-gray-200 rounded-md px-3 py-1.5 text-[13px] text-gray-900 focus:outline-none focus:ring-1 focus:ring-gray-300 transition-colors hover:bg-gray-100"
      >
        {selectedLabel}
        <svg
          className={`w-3.5 h-3.5 text-gray-400 transition-transform ${open ? "rotate-180" : ""}`}
          viewBox="0 0 16 16"
          fill="none"
          stroke="currentColor"
          strokeWidth="2"
          strokeLinecap="round"
          strokeLinejoin="round"
        >
          <path d="M4 6l4 4 4-4" />
        </svg>
      </button>

      {/* Mobile: invisible native select overlaid on trigger for OS picker */}
      {isTouch && (
        <select
          value={value}
          onChange={(e) => onChange(e.target.value)}
          className="absolute inset-0 w-full h-full opacity-0 cursor-pointer"
          aria-label={selectedLabel}
        >
          {options.map((opt) => (
            <option key={opt.value} value={opt.value}>{opt.label}</option>
          ))}
        </select>
      )}

      {/* Desktop: portal dropdown that floats over everything */}
      {!isTouch && open && pos && createPortal(
        <ul
          ref={listRef}
          className="fixed z-[9999] bg-white border border-gray-200 rounded-md shadow-lg max-h-60 overflow-y-auto"
          style={{ top: pos.top, left: pos.left, minWidth: pos.minWidth }}
        >
          {options.map((opt) => (
            <li
              key={opt.value}
              onClick={() => { onChange(opt.value); setOpen(false); }}
              className={`px-3 py-1.5 text-[13px] cursor-pointer transition-colors ${
                opt.value === value
                  ? "bg-gray-50 font-medium text-gray-900"
                  : "text-gray-700 hover:bg-gray-100"
              }`}
            >
              {opt.label}
            </li>
          ))}
        </ul>,
        document.body
      )}
    </div>
  );
}

